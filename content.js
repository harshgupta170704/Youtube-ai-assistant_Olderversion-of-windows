  async function callAI(systemPrompt, userPrompt, history){
    return new Promise((ok,fail)=>{
      chrome.runtime.sendMessage({
        type:"AI_REQUEST", provider:state.provider,
        payload:{systemPrompt,userPrompt,history},
      }, r=>{
        if(chrome.runtime.lastError) fail(new Error(chrome.runtime.lastError.message));
        else if(!r)         fail(new Error("No response. Reload page (F5)."));
        else if(!r.success) fail(new Error(r.error));
        else ok(r);
      });
    });
  }

