  function initDrag(panel) {
    const header = panel.querySelector(".ytai-header");
    if(!header) return;
    
    header.style.cursor = "move"; // Indicate grabbable area
    
    header.addEventListener("mousedown", e => {
      if(["BUTTON", "SVG", "PATH", "INPUT"].includes(e.target.tagName)) return;
      e.preventDefault();
      
      const sX = e.clientX, sY = e.clientY;
      const r = panel.getBoundingClientRect();
      const pW = panel.offsetWidth, pH = panel.offsetHeight;
      const bW = window.innerWidth, bH = window.innerHeight;
      
      const offX = sX - r.left;
      const offY = sY - r.top;
      
      panel.style.bottom = "auto";
      panel.style.right = "auto";
      panel.style.left = r.left + "px";
      panel.style.top = r.top + "px";
      panel.style.margin = "0";

      document.body.style.userSelect = "none";
      panel.style.transition = "none";

      const mv = me => {
        let nx = me.clientX - offX;
        let ny = me.clientY - offY;
        nx = Math.max(0, Math.min(bW - pW, nx));
        ny = Math.max(0, Math.min(bH - pH, ny));
        panel.style.left = nx + "px";
        panel.style.top = ny + "px";
      };

      const up = () => {
        document.removeEventListener("mousemove", mv);
        document.removeEventListener("mouseup", up);
        document.body.style.userSelect = "";
        panel.style.transition = "";
        sp("ytai_left", panel.style.left);
        sp("ytai_top", panel.style.top);
      };

      document.addEventListener("mousemove", mv);
      document.addEventListener("mouseup", up);
    });
    
    const savedLeft = localStorage.getItem("ytai_left");
    const savedTop = localStorage.getItem("ytai_top");
    if(savedLeft && savedTop) {
      panel.style.bottom = "auto";
      panel.style.right = "auto";
      panel.style.margin = "0";
      
      const bW = window.innerWidth, bH = window.innerHeight;
      let leftPx = parseInt(savedLeft) || 0;
      let topPx = parseInt(savedTop) || 0;
      
      leftPx = Math.max(0, Math.min(bW - panel.offsetWidth, leftPx));
      topPx = Math.max(0, Math.min(bH - panel.offsetHeight, topPx));
      
      panel.style.left = leftPx + "px";
      panel.style.top = topPx + "px";
    }
  }

  // ── Markdown ──────────────────────────────────────────────────────────────
  function mdToHtml(raw){
    let t=raw, blocks=[];
    t=t.replace(/```(\w*)\n?([\s\S]*?)```/g,(_,l,c)=>{blocks.push({lang:l||"code",code:c.trimEnd()});return`%%CB${blocks.length-1}%%`;});
    t=t.replace(/&/g,"&amp;").replace(/</g,"&lt;").replace(/>/g,"&gt;");
    t=t.replace(/\*\*(.+?)\*\*/g,"<strong>$1</strong>").replace(/__(.+?)__/g,"<strong>$1</strong>").replace(/\*(.+?)\*/g,"<em>$1</em>").replace(/`([^`]+)`/g,"<code>$1</code>");
    t=t.replace(/^#### (.+)$/gm,"<h4>$1</h4>").replace(/^### (.+)$/gm,"<h4>$1</h4>").replace(/^## (.+)$/gm,"<h3>$1</h3>").replace(/^# (.+)$/gm,"<h2>$1</h2>");
    t=t.replace(/^(\d+)\. (.+)$/gm,"<li>$2</li>").replace(/^[-*•] (.+)$/gm,"<li>$1</li>");
    t=t.replace(/((?:<li>.*?<\/li>\n?)+)/g,"<ul>$1</ul>");
    t=t.replace(/\n\n+/g,"</p><p>").replace(/\n/g,"<br>");
    t=t.replace(/<p><\/p>/g,"").replace(/<br>\s*<ul>/g,"<ul>").replace(/<\/ul>\s*<br>/g,"</ul>").replace(/<br>\s*<h/g,"<h");
    t=t.replace(/%%CB(\d+)%%/g,(_,i)=>{const{lang,code}=blocks[+i];const ec=code.replace(/&/g,"&amp;").replace(/</g,"&lt;").replace(/>/g,"&gt;");return`<div class="ytai-code-header"><span class="ytai-code-lang">${escHtml(lang)}</span><button class="ytai-copy-btn">📋 Copy</button></div><pre><code class="language-${lang}">${ec}</code></pre>`;});
    return t;
  }

