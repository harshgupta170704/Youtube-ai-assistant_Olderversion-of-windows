  /** Build topic chips from the video title */
  function updateSearchSuggestions(){
    const ctx=qs("#ytai-search-ctx"), chips=qs("#ytai-ctx-chips");
    if(!ctx||!chips||!state.videoTitle) return;

    // Extract key terms from video title (strip noise words, keep meaningful ones)
    const noiseWords = new Set(["the","a","an","and","or","in","on","at","for","to","of","with","by","from","is","how","what","why","when","this","that","all","full","course","tutorial","complete","guide","learn","beginner","advanced","#generativeai","#artificialintelligence"]);
    const terms = state.videoTitle
      .replace(/#\w+/g,"")           // strip hashtags
      .replace(/[^\w\s]/g," ")       // strip punctuation
      .split(/\s+/)
      .filter(w => w.length > 3 && !noiseWords.has(w.toLowerCase()))
      .slice(0, 6);

    if(!terms.length) return;

    chips.innerHTML = "";
    // Always add "overview" of the topic
    const titleShort = state.videoTitle.slice(0,40);
    const allChips = [titleShort, ...terms];

    allChips.slice(0,5).forEach(term => {
      const btn = document.createElement("button");
      btn.className = "ytai-ctx-chip";
      btn.textContent = term;
      btn.addEventListener("click", () => {
        qs("#ytai-search-input").value = term;
        runSearch();
      });
      chips.appendChild(btn);
    });
    ctx.style.display = "flex";
  }

  async function runSearch(){
    const q=qs("#ytai-search-input")?.value.trim();
    if(!q) return;
    const area=qs("#ytai-search-results");
    area.innerHTML=`<div class="ytai-search-loading">🔍 Searching with video context…</div>`;
    try {
      const res=await new Promise((ok,fail)=>{
        chrome.runtime.sendMessage({
          type:"SEARCH_REQUEST",
          payload:{
            query: q,
            videoTitle:       state.videoTitle       || "",
            videoDescription: state.videoDescription || "",
            channelName:      state.channelName      || "",
          },
        }, r=>{
          if(chrome.runtime.lastError) fail(new Error(chrome.runtime.lastError.message));
          else if(!r?.success)         fail(new Error(r?.error||"Search failed"));
          else ok(r.data);
        });
      });
      renderSearchResults(area, res, q);
    } catch(e){
      area.innerHTML=`<div class="ytai-empty"><p class="ytai-empty-title">Search failed</p><p class="ytai-empty-hint">${escHtml(e.message)}</p></div>`;
    }
  }

